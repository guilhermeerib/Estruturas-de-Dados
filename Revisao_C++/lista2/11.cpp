#include <iostream>
using namespace std;

int main() {
    cout << "Tamanho de int: " << sizeof(int) << " bytes" << endl;
    cout << "Tamanho de float: " << sizeof(float) << " bytes" << endl;
    cout << "Tamanho de double: " << sizeof(double) << " bytes" << endl;
    cout << "Tamanho de char: " << sizeof(char) << " byte" << endl;
    return 0;
}
